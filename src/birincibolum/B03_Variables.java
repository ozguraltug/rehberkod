package birincibolum;

public class B03_Variables {
    public static void main(String[] args) {


        int yas=20;
        System.out.println("Yaş : "+yas);

        double boy=1.70;

        System.out.println("Boy : "+boy);

        float boy2=1.75f;
        System.out.println("float boy :"+boy2);

        float floatPi=(float)Math.PI;
        double doublePi=Math.PI;
        System.out.println("float ile yazıldığında : "+floatPi);
        System.out.println("double ile yazıldığında : "+doublePi);

        boolean dogruMu=true;
        System.out.println(dogruMu);

        String str="Java Öğreniyorum";
        String yil="2023";
        System.out.println(str+" sene : "+yil);


    }
}
