package birincibolum;

public class B19_StringManupilations08Substring {
      /*
    substring: bir string üzerinde belirli bir aralıktaki karakterleri almanıza ve
    yeni bir alt dize oluşturmanıza olanak tanır.
     */
      public static void main(String[] args) {
          String str="Java OOP konsepti kullanır.";
          System.out.println(str.substring(5,11));
          System.out.println(str.substring(3,4));
          System.out.println(str.substring(8,8));
          System.out.println(str.substring(1));

      }

}
